    void validad(){
        String user = txtUsername.getText();
        String pass = txtPass.getText();
        
        if(txtUsername.getText().equals("") || txtPass.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Campos de textos vacios");
            txtUsername.requestFocus();
        }else{
            eu = udao.ValidarUser(user, pass);
            if(eu.getUsername() != null && eu.getPass() != null){
                idu = eu.getIdUser();
                System.out.println("id user " + idu);
                username = eu.getUsername();
                name = eu.getNombre();
                ape = eu.getApellido();
                tipou = eu.getTipouser();
                JOptionPane.showMessageDialog(null, "Bienvenido al Sitema " + user + " - " + name);
                Main main = new Main();
                main.setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(null, "Campos de Textos Incorrectos");
                txtUsername.requestFocus();
            }
        }
    }
    
